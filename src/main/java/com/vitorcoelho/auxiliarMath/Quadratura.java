    //Métodos públicos
    public static final double integral(double limiteInferior, double limiteSuperior, Funcao funcao, int nPontosGauss) {

        if (limiteInferior - limiteSuperior == 0.0) {
            return 0.0;
        }

        double a = (limiteSuperior + limiteInferior) / 2.0;
        double b = (limiteSuperior - limiteInferior) / 2.0;
        ConjuntoPontosDeGauss conjunto = pontosDeGauss.get(nPontosGauss - 1);
        double r[] = conjunto.getR();
        double w[] = conjunto.getW();

        double resultado = 0.0;
        double fxi;
        double xi;
        double ri;
        double wi;

        int j = 0;
        if (nPontosGauss % 2 != 0) {
            ri = r[0];
            wi = w[0];
            xi = a + b * ri;
            fxi = funcao.getFx(xi);
            resultado = wi * fxi;
            j = 1;
        }

        int m = (int) (0.5 * (nPontosGauss + 1));
        for (int i = j; i < m; i++) {
            ri = r[i];
            wi = w[i];
            xi = a + b * ri;
            fxi = funcao.getFx(xi);
            resultado += wi * fxi;

            ri = -ri;
            xi = a + b * ri;
            fxi = funcao.getFx(xi);
            resultado += wi * fxi;
        }

        resultado *= b;

        return resultado;
    }

