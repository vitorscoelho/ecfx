fun integrar(
    limiteInferior: Double, limiteSuperior: Double,
    qtdPontosGauss: Int,
    funcao: (x: Double) -> Double
): Double {
    require(qtdPontosGauss > 0) { "|qtdPontosGauss| deve ser maior do zero" }
    require(limiteSuperior >= limiteInferior) { "|limiteInferior| não pode ser maior do |limiteSuperior|" }
    if (limiteInferior - limiteSuperior == 0.0) return 0.0
    val coeficiente1 = (limiteSuperior + limiteInferior) / 2.0
    val coeficiente2 = (limiteSuperior - limiteInferior) / 2.0
    val pontos = pontosDeGauss(qtdPontos = qtdPontosGauss)
    val soma = pontos.sumByDouble { ponto ->
        val valor1 = ponto.w * funcao.invoke(coeficiente1 + coeficiente2 * ponto.r)
        if (ponto.r == 0.0) {
            valor1
        } else {
            valor1 + ponto.w * funcao.invoke(coeficiente1 - coeficiente2 * ponto.r)
        }
    }
    return coeficiente2 * soma
}

